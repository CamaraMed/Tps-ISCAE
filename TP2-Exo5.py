# Exo 5
# Initialisation des variables a et b
a = 0
b = 10

# Tant que a est inférieur à b incrémente a, puis affiche la valeur de a sur l'écran
print("Ci-dessous les différentes valeurs de a:")
while a < b:
    print(a)
    a = a + 1
print("Les valeurs de a ont été affichées avec succès \n")

print("Début d'affichage des valeurs de b")
# Tant que b>0 décrémente b puis affiche b sur l'écran
while b > 0:
    print(b)
    b -= 1
print("Les valeurs de b ont été affichées avec succès")
